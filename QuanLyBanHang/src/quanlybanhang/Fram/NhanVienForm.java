    private void jButton_ThemNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ThemNVActionPerformed
      
       
        conn = cn.getConnection();
        try {
            if(jTextFieldMaNV.getText().equals("") || jTextFieldTenNV1.getText().equals("")||jTextFieldNgaysinh.getText().equals("") 
                    ||jTextFieldEmail.getText().equals("") ||jTextFieldDiachi.getText().equals("")|| jTextFieldSDT.getText().equals("")){
             JOptionPane.showMessageDialog(this,"Bạn Cần cập nhật đủ dữ liệu ");
             }else{
                StringBuffer sb = new StringBuffer();
                String SQL_check_pk="SELECT MaNV FROM NHANVIEN WHERE MaNV = '"+jTextFieldMaNV.getText()+"'";
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(SQL_check_pk);
                if(rs.next()){
                    sb.append("Mã nhân viên đã bị trùng");
                }
                if(sb.length()>0){ //Nếu if kia đúng nó xẽ thêm vào sb 1 đoạn String , kiểm tra độ dài chuỗi này nếu lớn hơn 0 tức là có thông báo thì 
                    JOptionPane.showConfirmDialog(this,sb.toString());
                    rs.close();
                }else{
                   String sql = "INSERT INTO NHANVIEN VALUES(?,?,?,?,?,?,?)";
                   PreparedStatement pst = conn.prepareStatement(sql);
                   String manv = jTextFieldMaNV.getText();                   
                   String hoten = jTextFieldTenNV1.getText();
                   String ngaysinh = jTextFieldNgaysinh.getText();
                    String sdt = jTextFieldSDT.getText();
                   String sex = "";
                   if(rbtnNam.isSelected()){
                       sex = rbtnNam.getText();
                   }else{
                       sex = rbtnNu.getText();
                   }
                   String email = jTextFieldEmail.getText();
                   String diachi = jTextFieldDiachi.getText();
                   pst.setString(1, manv);                   
                   pst.setString(2, hoten);
                   pst.setString(3, ngaysinh);
                   pst.setString(4, email);
                   pst.setString(5, sex);
                    pst.setString(6, sdt);
                   pst.setString(7, diachi);
                   int kq = pst.executeUpdate();
                   if(kq>0){
                       JOptionPane.showMessageDialog(this, "Thêm mới thành công !");
                       LoatBang();
                       Xoatrang();
                           
                   }
                }
            conn.close();
        }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_jButton_ThemNVActionPerformed

 
    
