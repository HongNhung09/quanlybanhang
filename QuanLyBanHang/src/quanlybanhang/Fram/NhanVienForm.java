    private void jButton_TimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_TimkiemActionPerformed
          conn = cn.getConnection();
         try {
              if( jTextField_tennv.getText().equals("")){
             JOptionPane.showMessageDialog(this,"Nhập Tên nhân viên cần tìm kiếm ");
             }else{
            String sql = "SELECT *FROM NHANVIEN where TenNV like '%"+jTextField_tennv.getText()+"%' order by manv desc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            tb.setRowCount(0);
            while (rs.next()) {
               Object personObject[] = {
                  rs.getString("manv"),
                    rs.getString("tennv"),
                    rs.getString("ngaysinh"),
                    rs.getString("email"),
                    rs.getString("gioitinh"),
                    rs.getString("sdt"),
                    rs.getString("diachi")
                };
                tb.addRow(personObject);
                jTablenhanvien.setModel(tb);
                
              }
             rs.close();
           conn.close();
            }
          
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton_TimkiemActionPerformed

