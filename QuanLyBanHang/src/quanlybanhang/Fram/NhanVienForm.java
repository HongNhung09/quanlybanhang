     public void LoatBang(){
        try {
            conn = cn.getConnection();
            String sql = "SELECT *FROM NHANVIEN "
                    + "";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            tb.setRowCount(0);
            while (rs.next()) {
               Object personObject[] = {
                    rs.getString("manv"),
                    rs.getString("tennv"),
                    rs.getString("ngaysinh"),
                    rs.getString("email"),
                    rs.getString("gioitinh"),
                    rs.getString("sdt"),
                      rs.getString("diachi") 
                };
                tb.addRow(personObject);
                jTablenhanvien.setModel(tb);
            }
            
           st.close();
           rs.close();
           conn.close();
        } catch (Exception e) {
        }
   
        jTablenhanvien.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
               if (jTablenhanvien.getSelectedRow()>=0){
                   jTextFieldMaNV.setText(jTablenhanvien.getValueAt(jTablenhanvien.getSelectedRow(), 0)+"");
                   jTextFieldTenNV1.setText(jTablenhanvien.getValueAt(jTablenhanvien.getSelectedRow(), 1)+"");
                   jTextFieldNgaysinh.setText(jTablenhanvien.getValueAt(jTablenhanvien.getSelectedRow(), 2)+"");
                   //jTextFieldmakh.setText(jTable_Khachhang.getValueAt(jTable_Khachhang.getSelectedRow(), 3)+"");
                   jTextFieldEmail.setText(jTablenhanvien.getValueAt(jTablenhanvien.getSelectedRow(), 4)+"");
                   jTextFieldDiachi.setText(jTablenhanvien.getValueAt(jTablenhanvien.getSelectedRow(), 6)+"");
                    jTextFieldSDT.setText(jTablenhanvien.getValueAt(jTablenhanvien.getSelectedRow(), 5)+"");
               }
            }
        });
    }
